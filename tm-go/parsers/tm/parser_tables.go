func (n Symbol) String() string {
	if n < Symbol(terminalEnd) {
		return Token(n).String()
	}
	i := int(n) - int(terminalEnd)
	if i < len(symbolStr) {
		return symbolStr[i]
	}
	return fmt.Sprintf("nonterminal(%d)", n)
}

